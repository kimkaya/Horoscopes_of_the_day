-- 궁합 데이터베이스 테이블 생성

-- 띠 궁합 테이블
CREATE TABLE IF NOT EXISTS zodiac_compatibility (
    id INT AUTO_INCREMENT PRIMARY KEY,
    zodiac1 VARCHAR(20) NOT NULL COMMENT '첫 번째 띠',
    zodiac2 VARCHAR(20) NOT NULL COMMENT '두 번째 띠',
    compatibility_score INT NOT NULL COMMENT '궁합 점수 (0-100)',
    compatibility_level VARCHAR(20) NOT NULL COMMENT '궁합 등급',
    description TEXT NOT NULL COMMENT '궁합 설명',
    advice TEXT COMMENT '조언',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 별자리 궁합 테이블
CREATE TABLE IF NOT EXISTS star_sign_compatibility (
    id INT AUTO_INCREMENT PRIMARY KEY,
    sign1 VARCHAR(20) NOT NULL COMMENT '첫 번째 별자리',
    sign2 VARCHAR(20) NOT NULL COMMENT '두 번째 별자리',
    compatibility_score INT NOT NULL COMMENT '궁합 점수 (0-100)',
    compatibility_level VARCHAR(20) NOT NULL COMMENT '궁합 등급',
    description TEXT NOT NULL COMMENT '궁합 설명',
    love_score INT COMMENT '연애운 점수',
    friendship_score INT COMMENT '우정 점수',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

